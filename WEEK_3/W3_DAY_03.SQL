--1)WAQD EMPLOYEES NAME WITH MANAGER NAME.
SELECT M.EMPLOYEE_ID,M.FIRST_NAME AS EMPLOYEES_NAME,E.FIRST_NAME AS MANAGE_NAME
FROM EMPLOYEES E
JOIN EMPLOYEES M
ON(E.EMPLOYEE_ID=M.MANAGER_ID);

--2)WAQD MANAGER AND WHOW MANY EMPLOYEES WORK UNDER EACH MANAGER.
SELECT E.FIRST_NAME AS MANAGE_NAME,COUNT(M.EMPLOYEE_ID)AS TOTAL_EMP
FROM EMPLOYEES E
JOIN EMPLOYEES M
ON(E.EMPLOYEE_ID=M.MANAGER_ID)
GROUP BY E.FIRST_NAME;

--3)WAQD MANAGER AND WHOW MANY EMPLOYEES WORK UNDER EACH MANAGER AND MANAGER NAME START WITH 'A','K','M'.
SELECT E.FIRST_NAME AS MANAGE_NAME,COUNT(M.EMPLOYEE_ID)AS TOTAL_EMP
FROM EMPLOYEES E
JOIN EMPLOYEES M
ON(E.EMPLOYEE_ID=M.MANAGER_ID)
WHERE M.FIRST_NAME LIKE'A%'
OR M.FIRST_NAME LIKE'K%'
OR M.FIRST_NAME LIKE'M%'
GROUP BY E.FIRST_NAME;

--4)WAQD MANAGER AND WHOW MANY EMPLOYEES WORK UNDER EACH MANAGER AND SALARY UP TO 7000 AND COMMISSION IS NULL.
SELECT E.FIRST_NAME AS MANAGE_NAME,COUNT(M.EMPLOYEE_ID)AS TOTAL_EMP
FROM EMPLOYEES E
JOIN EMPLOYEES M
ON(E.EMPLOYEE_ID=M.MANAGER_ID)
WHERE E.SALARY>7000
AND E.COMMISSION_PCT IS NULL
GROUP BY E.FIRST_NAME;

--5)WAQD MANAGER FULL NAME AND SALARY.
SELECT (E.FIRST_NAME||' '||E.LAST_NAME)AS MANAGE_NAME,E.SALARY
FROM EMPLOYEES E
JOIN EMPLOYEES M
ON(E.EMPLOYEE_ID=M.MANAGER_ID)
GROUP BY (E.FIRST_NAME||' '||E.LAST_NAME),E.SALARY;


--6)WAQD THOSE EMPLOYEES WHOSE EARN MORE THAN SALARY.
SELECT M.FIRST_NAME AS MANAGER_NAME,M.SALARY AS MANAGER_SALARY,
E.FIRST_NAME AS EMPLOYEE_NAME,E.SALARY
FROM EMPLOYEES E
JOIN EMPLOYEES M
ON(E.EMPLOYEE_ID=M.MANAGER_ID)
WHERE E.SALARY>M.SALARY;

--7)WAQD THOSE EMPLOYEES WHOSE SALARY BETWEEN 8000 TO 12000.
SELECT M.FIRST_NAME AS MANAGER_NAME,
E.FIRST_NAME AS EMPLOYEE_NAME,E.SALARY
FROM EMPLOYEES E
JOIN EMPLOYEES M
ON(E.EMPLOYEE_ID=M.MANAGER_ID)
WHERE E.SALARY BETWEEN 8000 AND 12000;

--8)WAQD MANAGER FULL NAME AND SALARY AND SALARY MORE THAN 9000.
SELECT (E.FIRST_NAME||' '||E.LAST_NAME)AS MANAGE_NAME,E.SALARY
FROM EMPLOYEES E
JOIN EMPLOYEES M
ON(E.EMPLOYEE_ID=M.MANAGER_ID)
WHERE E.SALARY>9000
GROUP BY (E.FIRST_NAME||' '||E.LAST_NAME),E.SALARY;

--9)WAQD THOSE EMPLOYEES WITH MANAGER_NAME WHOSE MANAGER_HIRE DATE BETWEEN 2005 TO 2010.
SELECT M.EMPLOYEE_ID,M.FIRST_NAME AS EMPLOYEES_NAME,
E.FIRST_NAME AS MANAGE_NAME,M.HIRE_DATE AS MANAGER_HIRE_DATE
FROM EMPLOYEES E
JOIN EMPLOYEES M
ON(E.EMPLOYEE_ID=M.MANAGER_ID)
WHERE TO_CHAR(E.HIRE_DATE,'YYYY') BETWEEN '2005' AND '2010';
DESC EMPLOYEES;

--10)WAQD THOSE EMPLOYEES WITH MANAGER_NAME WHOSE EMPLOYEES_HIRE DATE UP TO 2005.
SELECT M.EMPLOYEE_ID,M.FIRST_NAME AS EMPLOYEES_NAME,
E.FIRST_NAME AS MANAGE_NAME,E.HIRE_DATE AS EMP_HIRE_DATE
FROM EMPLOYEES E
JOIN EMPLOYEES M
ON(E.EMPLOYEE_ID=M.MANAGER_ID)
WHERE TO_CHAR(E.HIRE_DATE,'YYYY')>'2005';
DESC EMPLOYEES;
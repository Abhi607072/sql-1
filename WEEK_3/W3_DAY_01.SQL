--TOPIC.INER JOIN
--1)WAQD THOSE EMPLOYEES WHOSE DEPARTMENT NAME 'Marketing','IT','Sales','Finance';
SELECT E.EMPLOYEE_ID,E.FIRST_NAME,E.LAST_NAME,E.SALARY,E.DEPARTMENT_ID,D.DEPARTMENT_NAME
FROM EMPLOYEES E
JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
WHERE DEPARTMENT_NAME IN('Marketing','IT','Sales','Finance');

--2)WAQD THOSE EMPLOYEES WORKING IN 'Marketing','IT','Sales','Finance'AND HAVING 12000 SALARY.
SELECT E.EMPLOYEE_ID,E.FIRST_NAME,E.LAST_NAME,E.SALARY,E.DEPARTMENT_ID,D.DEPARTMENT_NAME
FROM EMPLOYEES E
JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
WHERE DEPARTMENT_NAME IN('Marketing','IT','Sales','Finance')
AND E.SALARY>12000;

--3)WAQD THOSE EMPLOYEES WHERE SALARY GREATER THAN 7000 AND SHOW EXPERIENCE UP TO 13 AND SHOW DEPARTMENT NAME AND EMPLOYEES DETAILS.
SELECT E.EMPLOYEE_ID,E.FIRST_NAME,E.LAST_NAME,E.SALARY,E.DEPARTMENT_ID,D.DEPARTMENT_NAME
FROM EMPLOYEES E
JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
WHERE SALARY > 7000;

--4)WAQD EMPLOYEES WHOSE LAST_NAME START WITH 'A' OR 'T' AND SHOW DEPARTMENT NAME.
SELECT E.EMPLOYEE_ID,E.FIRST_NAME,E.LAST_NAME,E.SALARY,E.DEPARTMENT_ID,D.DEPARTMENT_NAME
FROM EMPLOYEES E
JOIN DEPARTMENTS D
ON (E.DEPARTMENT_ID=D.DEPARTMENT_ID)
WHERE E.LAST_NAME LIKE'A%'
OR E.LAST_NAME LIKE'T%';

--5)WAQD EMPLOYEES FULL NAME  WHOSE HAVING SALARY GREATER THAN 7000 AND DEPARTMENT NAME IS NOT 'Public Relations'AND 'Executive'.
SELECT E.EMPLOYEE_ID,CONCAT(CONCAT(E.FIRST_NAME,' '),
E.LAST_NAME)AS FULL_NAME,E.SALARY,D.DEPARTMENT_NAME
FROM EMPLOYEES E
JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
WHERE D.DEPARTMENT_NAME NOT IN('Public Relations','Executive')
AND E.SALARY>7000;

--6)WAQD THASE EMPLOYEES WHOSE JOB_ID IN 'IT_PROG','PU_MAN','PU_MAN' AND SALARY GREATER THAN 3000 AND LOCATION ID IS 1700 AND 1800.
SELECT E.EMPLOYEE_ID,E.FIRST_NAME,E.LAST_NAME,E.SALARY,E.JOB_ID,D.LOCATION_ID
FROM EMPLOYEES E
JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
WHERE JOB_ID IN('IT_PROG','PU_MAN','PU_MAN')
AND SALARY >3000
AND LOCATION_ID IN(1700,1800);

--7)WAQD DEPARTMENT VIZ COUNT HOW MANY EMPLOYEES WORKING IN EACH DEPARTMENT ORDER BY DEPARTMENT_NAME.
SELECT D.DEPARTMENT_NAME,COUNT(E.EMPLOYEE_ID)
FROM EMPLOYEES E
JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
GROUP BY D.DEPARTMENT_NAME
ORDER BY D.DEPARTMENT_NAME;

--8)WAQD THOSE EMPLOYEES WITH THERE DEPARTMENT NAME AND SHOW ANUAL SALARY.
SELECT E.EMPLOYEE_ID,E.FIRST_NAME,E.LAST_NAME,D.DEPARTMENT_NAME,
E.SALARY AS MON_SALALARY,SALARY*12 AS ANUAL_SALARY
FROM EMPLOYEES E
JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID);


--9)WAQD DEPARTMENT VIZ COUNT HOW MANY EMPLOYEES WORKING IN EACH DEPARTMENT AND SHOW ONLY THOSE DEPARTMENT COUNT GREATER THAN 10 ORDER BY DEPARTMENT_NAME.
SELECT D.DEPARTMENT_NAME,COUNT(E.EMPLOYEE_ID)AS TOTAL
FROM EMPLOYEES E
JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
GROUP BY D.DEPARTMENT_NAME
HAVING COUNT(E.EMPLOYEE_ID)>10
ORDER BY D.DEPARTMENT_NAME;

--10)WAQD EMPLOYEES WHOSE DEPARTMENT LOCATIONS ID IS 1700,1800 AND SALARY UP TO 10000.
SELECT E.EMPLOYEE_ID,E.FIRST_NAME,SALARY,D.DEPARTMENT_NAME,D.LOCATION_ID
FROM EMPLOYEES E
JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
WHERE E.SALARY>10000;

--11)WAQD EMPLOYEES ANUAL SALARY WITH COMMISSION AND SHOW AVRAGE SALARY FOR EACH DEPARTMENTS.
SELECT D.DEPARTMENT_NAME,ROUND(AVG(SALARY+SALARY*NVL(COMMISSION_PCT,0)/12))AS ANUAL_SALARY
FROM EMPLOYEES E
JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
GROUP BY D.DEPARTMENT_NAME;

--12)WAQD THOSE EMPLOYEES WITH THERE DEPARTMENT NAME AND SHOW ANUAL SALARY GREATER THAN 200000 SORT DATA ASC.
SELECT E.EMPLOYEE_ID,E.FIRST_NAME,E.LAST_NAME,D.DEPARTMENT_NAME,
E.SALARY AS MON_SALALARY,SALARY*12 AS ANUAL_SALARY
FROM EMPLOYEES E
JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
WHERE SALARY*12>200000
ORDER BY SALARY;

--13)WAQD EMPLOYEES DEPARTMENT NAME VIZ STARTING DATE
SELECT D.DEPARTMENT_NAME,MIN(HIRE_DATE)AS START_DATE
FROM EMPLOYEES E
JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
GROUP BY D.DEPARTMENT_NAME
ORDER BY MIN(HIRE_DATE) ;

--14)WAQD EMPLOYEES FULL NAME IN UPPER CASE WHOSE HAVING SALARY GREATER THAN 7000 AND COMMISSION PCT IS NOT NULL.
SELECT E.EMPLOYEE_ID,UPPER(CONCAT(CONCAT(E.FIRST_NAME,' '),
E.LAST_NAME))AS FULL_NAME,E.SALARY,D.DEPARTMENT_NAME
FROM EMPLOYEES E
JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
WHERE COMMISSION_PCT IS NOT NULL
AND E.SALARY>7000;

--15)WAQD DEPARTMENT VIZ COUNT HOW MANY EMPLOYEES WORKING IN EACH DEPARTMENT AND ONLY SHOW EVEN NUMBERS OF COUNT ORDER BY DEPARTMENT_NAME.
SELECT D.DEPARTMENT_NAME,COUNT(E.EMPLOYEE_ID)AS COUNT_EMP
FROM EMPLOYEES E
JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
GROUP BY D.DEPARTMENT_NAME
HAVING MOD(COUNT(E.EMPLOYEE_ID),2)=0
ORDER BY D.DEPARTMENT_NAME;

--16)WAQD LOCATION VIZ DEPARTMENT COUNT AND EMPLOYEES COUNT SORT EMPLOYEES VIZ COUNT.
SELECT D.LOCATION_ID,D.DEPARTMENT_NAME,COUNT(E.EMPLOYEE_ID)AS COUNT_EMP
FROM EMPLOYEES E
JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
GROUP BY D.LOCATION_ID,D.DEPARTMENT_NAME
ORDER BY COUNT(E.EMPLOYEE_ID);

--17)WAQD THOSE EMPLOYEES WHOSE FULL NAME LENGTH GREATER THAN 10 AND SALARY GREATER THAN 5000 SORT SALARY ASCENDING. 
SELECT CONCAT(CONCAT(E.FIRST_NAME,' '),E.LAST_NAME)AS FULL_NAME,
LENGTH(CONCAT(E.FIRST_NAME,E.LAST_NAME))AS NAME_LENGTH,E.SALARY,D.DEPARTMENT_ID,D.DEPARTMENT_NAME
FROM EMPLOYEES E
JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
WHERE (LENGTH(CONCAT(E.FIRST_NAME,E.LAST_NAME)))>10
AND SALARY>5000
ORDER BY SALARY ASC;

--18)WAQD FULL NAME SALARY AND DEPARTMENT ID,DEPARTMENT_NAME ONLY THOSE EMPLOYEES HAVING COMMISSION_PCT ANS ORDER BY SALARY DESCENDING.
SELECT CONCAT(CONCAT(E.FIRST_NAME,' '),E.LAST_NAME)AS FULL_NAME,
E.SALARY,E.COMMISSION_PCT,D.DEPARTMENT_ID,D.DEPARTMENT_NAME
FROM EMPLOYEES E
JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
WHERE COMMISSION_PCT IS NOT NULL
ORDER BY SALARY DESC;

--19)WAQD THOSE IS EMPLOYEES HAVING COMMISSION_PCT ADD '.1'PARSENT AND THOSE NOT GET COMMISSION ADD '.2'PARSENT SORTED BY COMMISSION.
SELECT E.EMPLOYEE_ID,E.FIRST_NAME,E.LAST_NAME,E.SALARY,
NVL2(E.COMMISSION_PCT,E.COMMISSION_PCT+.1,.2)AS COMMISSION_PCT,DEPARTMENT_NAME
FROM EMPLOYEES E
JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
ORDER BY COMMISSION_PCT DESC;

--20)WAQD THOSE IS EMPLOYEES HAVING SALARY ADD COMMISSION_PCT AND SHOW THOSE EMPLOYEES DEPARTMENTS NAME ORDER BY SALARY. 
SELECT E.EMPLOYEE_ID,E.FIRST_NAME,E.LAST_NAME,E.SALARY,D.DEPARTMENT_NAME,
NVL2(E.COMMISSION_PCT,E.SALARY+E.SALARY*E.COMMISSION_PCT,E.SALARY+E.SALARY*0)AS TOTAL_SALARY,DEPARTMENT_NAME
FROM EMPLOYEES E
JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
ORDER BY SALARY DESC;

--10)WAQD EMPLOYEES DETAILS AND DEPARTMENT_ID AND NAME WITH CITY AND ONLY SHOW DEPARTMENT LOCATED NOT IN 'Seattle','South San Francisco','Oxford','Oxford'AND SALARY MORE THAN 12000.

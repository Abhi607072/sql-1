--TOPIC.LEFT OUTER JOIN,RIGHT OUTER JOIN,FULL OUTER JOIN,SELF JOIN,INNER JOIN.

--1)WAQD EMPLOYEES DETAILS AND DEPARTMENT NAME AND LOCATION_ID.
SELECT E.EMPLOYEE_ID,E.FIRST_NAME,E.LAST_NAME,E.SALARY,D.DEPARTMENT_NAME,D.LOCATION_ID
FROM EMPLOYEES E
LEFT OUTER JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID);

--2)WAQD EMPLOYEES DETAILS AND DEPARTMENT NAME AND LOCATION_ID AND SHOW ONLY THOSE EMPLOYEES HAVING SALARY MORE THAN 7000 SORT DATA.
SELECT E.EMPLOYEE_ID,E.FIRST_NAME,E.LAST_NAME,E.SALARY,D.DEPARTMENT_NAME,D.LOCATION_ID
FROM EMPLOYEES E
LEFT OUTER JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
WHERE E.SALARY>7000
ORDER BY E.EMPLOYEE_ID;

--3)WAQD EMPLOYEES NAME AND DEPARTMENT NAME INCLUDING EMPLOYEES WITHOUT DEPARTMENT AND EPARTMENT WITH OUT EMPLOYEES.
SELECT (E.FIRST_NAME||' '||LAST_NAME)AS EMPLOYEES_NAME,D.DEPARTMENT_NAME
FROM EMPLOYEES E 
FULL OUTER JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID);


--4)WAQD EMPLOYEES WHO ARE NOT ASSIGNED TO ANY DEPARTMENTS.
SELECT (E.FIRST_NAME||' '||LAST_NAME)AS EMPLOYEES_NAME,D.DEPARTMENT_NAME
FROM EMPLOYEES E 
FULL OUTER JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
WHERE D.DEPARTMENT_ID IS NULL;

--5)WAQD THOSE DEPARTMENTS WHICH DO NOT HAVE ANY EMPLOYEES.
SELECT D.DEPARTMENT_NAME,COUNT(E.EMPLOYEE_ID)AS TOTAL_EMP
FROM EMPLOYEES E 
FULL OUTER JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
GROUP BY D.DEPARTMENT_NAME
HAVING COUNT(E.EMPLOYEE_ID)=0;

--6)WAQD MANAGER AND WHOW MANY EMPLOYEES WORK UNDER EACH MANAGER AND SALARY UP TO 7000 AND COMMISSION IS NULL.
SELECT E.FIRST_NAME AS MANAGE_NAME,COUNT(M.EMPLOYEE_ID)AS TOTAL_EMP
FROM EMPLOYEES E
JOIN EMPLOYEES M
ON(E.EMPLOYEE_ID=M.MANAGER_ID)
WHERE E.SALARY>7000
AND E.COMMISSION_PCT IS NOT NULL
GROUP BY E.FIRST_NAME;

--7)WAQD EMPLOYEES DETAILS AND DEPARTMENT_ID AND NAME WITH CITY.
SELECT E.EMPLOYEE_ID,E.FIRST_NAME,E.LAST_NAME,E.SALARY,D.DEPARTMENT_NAME,L.CITY
FROM EMPLOYEES E
JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
JOIN LOCATIONS L
ON(D.LOCATION_ID=L.LOCATION_ID);

--8)WAQD EMPLOYEES DETAILS AND DEPARTMENT_ID AND NAME WITH CITY AND ONLY SHOW DEPARTMENT LOCATED NOT IN 'Seattle','South San Francisco','Oxford','Oxford'AND SALARY MORE THAN 12000.
SELECT E.EMPLOYEE_ID,E.FIRST_NAME,E.LAST_NAME,E.SALARY,D.DEPARTMENT_NAME,L.CITY

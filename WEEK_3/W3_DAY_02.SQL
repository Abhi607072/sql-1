--TOPIC.LEFT OUTER JOIN,RIGHT OUTER JOIN,FULL OUTER JOIN.

--1)WAQD ALL EMPLOYEES DETAILS WITH DEPARTMENT_NAME.
SELECT E.*,D.DEPARTMENT_NAME
FROM EMPLOYEES E 
LEFT JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID);

--2)WAQD ONLY THOSE EMPLOYEES HAVING COMMISSION AND SALARY GREATER THAN 7000.
SELECT E.EMPLOYEE_ID,E.FIRST_NAME,E.LAST_NAME,E.SALARY,E.COMMISSION_PCT,D.DEPARTMENT_NAME
FROM EMPLOYEES E 
LEFT JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
WHERE COMMISSION_PCT IS NOT NULL
AND SALARY>7000;

--3)WAQD THOSE EMPLOYEES DETAILS WHOSE WORKING IN DEPARTMENT 'Administration','IT','Accounting','Sales'.
SELECT E.EMPLOYEE_ID,E.FIRST_NAME,E.LAST_NAME,E.SALARY,E.COMMISSION_PCT,D.DEPARTMENT_NAME
FROM EMPLOYEES E 
LEFT JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
WHERE DEPARTMENT_NAME IN('Administration','IT','Accounting','Sales');

--4)WAQD THOSE EMPLOYEES DEPARTMENT NAMES AND COUNT OF EMPLOYEES WORKING IN DEPARTMENT 'Administration','IT','Accounting','Sales'AND SORT THE DATA ASCENDING.
SELECT D.DEPARTMENT_NAME,COUNT(E.EMPLOYEE_ID)AS TOTAL_EMP,ROUND(AVG(E.SALARY))AS AVG_SALARY
FROM EMPLOYEES E 
LEFT JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
WHERE DEPARTMENT_NAME IN('Administration','IT','Accounting','Sales')
GROUP BY D.DEPARTMENT_NAME
ORDER BY COUNT(E.EMPLOYEE_ID);

--5)WAQD THOSE DEPARTMENTS WHOSE WORKING MINMUM 3 EMPLOYEES AND SORT THE DATA IN DESCENDING.
SELECT D.DEPARTMENT_NAME,COUNT(E.EMPLOYEE_ID)AS TOTAL_EMP
FROM EMPLOYEES E 
RIGHT JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
GROUP BY D.DEPARTMENT_NAME
HAVING COUNT(E.EMPLOYEE_ID)>=3
ORDER BY COUNT(E.EMPLOYEE_ID)DESC;

--6)WAQD THOSE DEPARTMENTS WHOSE WORKING MINMUM 3 EMPLOYEES WITH LOCATION_ID AND SORT THE DATA IN DESCENDING.
SELECT D.DEPARTMENT_NAME,D.LOCATION_ID,COUNT(E.EMPLOYEE_ID)AS TOTAL_EMP
FROM EMPLOYEES E 
RIGHT JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
GROUP BY D.DEPARTMENT_NAME,D.LOCATION_ID
HAVING COUNT(E.EMPLOYEE_ID)>=3
ORDER BY COUNT(E.EMPLOYEE_ID)DESC;

--7)WAQD THOSE EMPLOYEES AND DEPARTMENTS DETAILS WHOSE WORKING IN DEPARYMENT 'Administration','IT','Accounting','Sales' AND SHOW MINMUM AND MXZIMUM SALARY. 
SELECT D.DEPARTMENT_NAME, 
MIN(E.SALARY)AS MIN_SALARY,
MAX(E.SALARY)AS MAX_SALARY
FROM EMPLOYEES E 
FULL JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
WHERE DEPARTMENT_NAME IN('Administration','IT','Accounting','Sales-')
GROUP BY D.DEPARTMENT_NAME;

--8)WAQD EMPLOYEES NAME AND DEPARTMENT NAME INCLUDING EMPLOYEES WITHOUT DEPARTMENT AND EPARTMENT WITH OUT EMPLOYEES.
SELECT (E.FIRST_NAME||' '||LAST_NAME)AS EMPLOYEES_NAME,D.DEPARTMENT_NAME
FROM EMPLOYEES E 
FULL OUTER JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID);


--9)WAQD EMPLOYEES WHO ARE NOT ASSIGNED TO ANY DEPARTMENTS.
SELECT (E.FIRST_NAME||' '||LAST_NAME)AS EMPLOYEES_NAME,D.DEPARTMENT_NAME
FROM EMPLOYEES E 
FULL OUTER JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
WHERE D.DEPARTMENT_ID IS NULL;

--10WAQD THOSE DEPARTMENTS WHICH DO NOT HAVE ANY EMPLOYEES.
SELECT D.DEPARTMENT_NAME,COUNT(E.EMPLOYEE_ID)AS TOTAL_EMP
FROM EMPLOYEES E 
FULL OUTER JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
GROUP BY D.DEPARTMENT_NAME
HAVING COUNT(E.EMPLOYEE_ID)=0;

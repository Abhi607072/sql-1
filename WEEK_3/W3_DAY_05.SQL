--17)WAQD COUNTRY NAME WISE GRADE WISE COUNT OF EMPLOYEES HAVING MORE THAN 2 EMPLOYEES
SELECT C.COUNTRY_NAME,G.GRADE,COUNT(*) AS EMPLOYEES_COUNT
FROM EMPLOYEES E JOIN SALARY_GRADE G
ON (E.SALARY BETWEEN G.MIN_SALARY AND G.MAX_SALARY)
JOIN DEPARTMENTS D
ON (E.DEPARTMENT_ID = D.DEPARTMENT_ID)
JOIN LOCATIONS L
ON (D.LOCATION_ID = L.LOCATION_ID)
JOIN COUNTRIES C
ON (L.COUNTRY_ID = C.COUNTRY_ID)
GROUP BY C.COUNTRY_NAME,G.GRADE
HAVING COUNT(*) > 2;

--18)WAQD EMPLOYEES DETAILS ALONG WITH SALARYS GREADE STATUSE AND HIRE IN 2005.

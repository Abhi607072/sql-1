--5)WAQD EMPLOYEES DETAILS WITH DEPARTMENT_ NAME AND COUNTRY NAME WHOSE SALARY MORE THAN 12000 AND WHICH NOT GET COMMISSION_PCT AND SHOW ONLY THOSE EMPLOYEES WHICH LOCATED IN 'Roma','Southlake','Oxford'.

FROM EMPLOYEES E
JOIN SALARY_GRADE S
ON(E.SALARY BETWEEN S.MIN_SALARY AND S.MAX_SALARY)
WHERE TO_CHAR(E.HIRE_DATE,'YYYY')='2005'
ORDER BY E.EMPLOYEE_ID;


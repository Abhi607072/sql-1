--1)WAQD EMPLOYEES DETAILS WITH DEPARTMEMT_NAME AND CITY.
SELECT E.EMPLOYEE_ID,E.FIRST_NAME,E.LAST_NAME,E.SALARY,D.DEPARTMENT_NAME,L.CITY
FROM EMPLOYEES E JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
JOIN LOCATIONS L
ON(D.LOCATION_ID=L.LOCATION_ID);

--2)WAQD EMPLOYEES DETAILS WITH DEPARTMENT_NAME AND SHOW ONLY THOSE EMPLOYEES WHICH LOCATED IN 'Roma','Southlake','Oxford'. 
SELECT E.EMPLOYEE_ID,E.FIRST_NAME,E.LAST_NAME,E.SALARY,D.DEPARTMENT_NAME,L.CITY
FROM EMPLOYEES E JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)

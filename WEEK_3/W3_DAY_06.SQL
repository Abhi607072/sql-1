--6)WAQD THOSE EMPLOYEES WHOSE HAVING SAME CITY EQUAL TO  Steven AND SALARY GERATERR THAN AND CITY WELL BE 'South San Francisco'.
SELECT E.EMPLOYEE_ID,E.FIRST_NAME,E.LAST_NAME,E.SALARY,L.CITY
FROM EMPLOYEES E JOIN DEPARTMENTS D 
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
JOIN LOCATIONS L
ON (D.LOCATION_ID=L.LOCATION_ID)
WHERE E.SALARY >ANY (SELECT SALARY
                   FROM EMPLOYEES
                   WHERE FIRST_NAME='Steven')
AND L.CITY=(SELECT CITY
            FROM LOCATIONS
            WHERE CITY='South San Francisco') ;


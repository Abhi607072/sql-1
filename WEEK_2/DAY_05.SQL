--1)WQAD DEPARTMENT VIZ COUNT AND DISPLAY OMLY THOSE DEPARTMENT WHERE COUNT IS MORE THAN 5.
SELECT DEPARTMENT_ID,COUNT(*)AS TOTAL_EMP
FROM EMPLOYEES
GROUP BY DEPARTMENT_ID
HAVING COUNT(*)>5;

--2)WAQD TO FIND MANAGER WHOSE UNDER EMPLOYEES GRATER THAN 5
SELECT MANAGER_ID,COUNT(*)AS TOTAL_EMP
FROM EMPLOYEES
GROUP BY MANAGER_ID
HAVING COUNT(*)>5;

--3)WAQD TO FIND JOB_ID WORKING EMPLOYEES GREATER THAN GREATER THAN 3.
SELECT JOB_ID,COUNT(*)AS TOTAL_EMP
FROM EMPLOYEES
GROUP BY JOB_ID
HAVING COUNT(*)>3;

--4)WAQD THOSE EMPLOYEES WHOSE MANAGER VIZ AVRAGE SALARY.
SELECT MANAGER_ID,ROUND(AVG(SALARY))AS AVG_SALARY
FROM EMPLOYEES

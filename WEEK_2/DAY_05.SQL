--25)WAQD SHOWS THOSE EMPLOYEE HIRED SATURDAY,THURSDAY,WEDNESDAY,TUESDAY,SUNDAY AND DAY VIZ COUNT SORTED BY TOTAL COUNT IN DESCENDING.
SELECT TO_CHAR(HIRE_DATE,'DAY')AS WEEK_DAY,COUNT(*)AS TOTAL_EMP 
FROM EMPLOYEES 
GROUP BY TO_CHAR(HIRE_DATE,'DAY')
HAVING TRIM(TO_CHAR(HIRE_DATE,'DAY')) 
IN('SATURDAY','THURSDAY','WEDNESDAY','TUESDAY','SUNDAY')
ORDER BY COUNT(*)DESC;
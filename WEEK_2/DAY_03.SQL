--22)WAQD ALL EMPLOYEES DITAILS IN UPPER CASE WHOSE DEPARTMENT_ID IS NOT IN 90, 50, 60, WHOSE HIRE_DATE IS GETER THAN 07-03-2006,CALCULEAT YEARS OF SERVICS, AND ORBER BY THE RESULT BY HIRE_DATE.
SELECT EMPLOYEE_ID,UPPER(FIRST_NAME)AS FIRST_NAME,UPPER(LAST_NAME)AS LAST_NAME,
UPPER(EMAIL)AS EMAIL,UPPER(JOB_ID)AS JOB_ID,SALARY,DEPARTMENT_ID,
ROUND(MONTHS_BETWEEN(SYSDATE, HIRE_DATE) / 12, 2) AS YEARS
FROM EMPLOYEES
WHERE DEPARTMENT_ID NOT IN (90, 50, 60)
AND HIRE_DATE > TO_DATE('07-03-2006', 'DD-MM-YYYY')
ORDER BY HIRE_DATE; 

--23)WQAD EMPLOYEES SALARY AND CONVERT INTO USA DOLLAR AND DISPLAY HIRE_DATE AND MONTH.
SELECT 
EMPLOYEE_ID,LAST_NAME,SALARY,HIRE_DATE,TO_CHAR(HIRE_DATE,'MONTH')AS JOIN_MONTH,
CONCAT(ROUND(SALARY / 83, 2),'$') AS DOLLAR
FROM EMPLOYEES;

--24)WQAD EMPLOYEES WHOSE EXPERIENCE IS MORE THAN 50 MONTHS AND YEAR OF HIRE_DATE.
SELECT EMPLOYEE_ID,FIRST_NAME,
ROUND(MONTHS_BETWEEN(SYSDATE, HIRE_DATE))AS MONTHS_EMP,
TO_CHAR(HIRE_DATE,'YYYY')AS YEAR_HIRE
FROM EMPLOYEES
WHERE MONTHS_BETWEEN(SYSDATE, HIRE_DATE) > 50;


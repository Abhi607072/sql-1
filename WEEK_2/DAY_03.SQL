--20)WAQD THOSE FULL NAME EMPLOYEES WHOSE NOT GET COMMISSION AND SALARY MORE THAN 6000, THOSE EMPLOYEES GIVE COMMISSION .2  AND TOTAL SALARY, SORT IN ASCENDING ORDER.
SELECT EMPLOYEE_ID,CONCAT(CONCAT(FIRST_NAME,' '),LAST_NAME)AS FULL_NAME,
SALARY,COMMISSION_PCT,NVL(COMMISSION_PCT,.2)AS NEW_PCT,
SALARY*NVL(COMMISSION_PCT,.2)AS TOTAL_SALARY
FROM EMPLOYEES
WHERE SALARY>6000
AND COMMISSION_PCT IS NULL
ORDER BY COMMISSION_PCT ASC;


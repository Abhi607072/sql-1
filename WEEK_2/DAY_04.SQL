--MULTI-ROW_FUNCTION(SUM,AVG,COUNT,MIN,MAX),GROUP BY CLAUSE.
--1)WAQD EMPLOYEES JOB_ID AND NUMDER OF EMPLOYEES WORKING
SELECT JOB_ID,COUNT(*)AS TOTAL
FROM EMPLOYEES
GROUP BY JOB_ID;

--2)WAQD EMPLOYEES AVRAGE INCOME .
SELECT ROUND(AVG(SALARY))AS AVG
FROM EMPLOYEES


--3)WAQD DEPARTMENTS AND HOW MANY EMPLOYEES WORKING IN IN EACH DEPARTMENTS.
SELECT DEPARTMENT_ID,COUNT(*)AS COUNTS
FROM EMPLOYEES
WHERE DEPARTMENT_ID IS NOT NULL
GROUP BY DEPARTMENT_ID;

--4)WAQD GROUPS OF SALARY AND COUNT TOTAL EMPLOYEES HAVING SALARY >7000.  
SELECT SALARY,COUNT(*)AS TOTAL_EMPLOYEES
FROM EMPLOYEES
WHERE SALARY >7000
GROUP BY SALARY;

--5)WAQD EMPLOYEES JOB_ID AND JOB_ID VIZE  AVRAGE SALARY.
SELECT JOB_ID,AVG(SALARY)AS AVG_SALARY
FROM EMPLOYEES
GROUP BY JOB_ID;

--6)WAQ TO FIND PAID AVRAGE SALARY AS PER JOB_ID.
SELECT JOB_ID,AVG(SALARY) AS AVG_SALARY
FROM EMPLOYEES
GROUP BY JOB_ID;

--7)WAQD MANAGER AND THERE SALARY and COUNT THE TOTAL NUMBER OF EMPLOYEES AND DISPLAY AVRAGE SALARY.
SELECT MANAGER_ID,COUNT(*)AS TOTAL_NUM_EMP,ROUND(AVG(SALARY))AS AVG_SALARY
FROM EMPLOYEES
GROUP BY MANAGER_ID
ORDER BY MANAGER_ID;

--8)WAQD ALL UNIQUES DEPARTMENT ID COUNT JOB_ID FROM EMPLOYEES
SELECT DISTINCT DEPARTMENT_ID,COUNT(JOB_ID) 
FROM EMPLOYEES
GROUP BY DEPARTMENT_ID;

--9)WAQD EMPLOYEES WHOSE JOIN IN JUNE
SELECT DEPARTMENT_ID,COUNT(*)AS TOTAL_DEP
FROM EMPLOYEES
WHERE TRIM(TO_CHAR(HIRE_DATE,'MONTH'))='JUNE'
GROUP BY DEPARTMENT_ID,TO_CHAR(HIRE_DATE,'MONTH');

--10)WAQD EMPLOYEES THOSE EXPERIENCE GREATER THAN 22 YEAR AND DATE VIZ COUNT.
SELECT HIRE_DATE,COUNT(*)AS TOTAL_EMPLOYEES
FROM EMPLOYEES
WHERE ROUND(MONTHS_BETWEEN(SYSDATE,HIRE_DATE)/12)>22
GROUP BY HIRE_DATE;


--11)WAQD EMPLOYEES COUNT WHO HAS SAME AMOUNT OF SALARY AND SORT BY HIGHEST COUNT TO LOWEST COUNT
SELECT SALARY,COUNT(*) AS EMPLOYEES_COUNT
FROM EMPLOYEES
GROUP BY SALARY
ORDER BY EMPLOYEES_COUNT DESC;

--12)WAQD COUNT OF DEPARTMENT WHICH LOCATED IN SAME PLACE
SELECT LOCATION_ID,COUNT(*) AS SAME_LOCATION_COUNT
FROM DEPARTMENTS
GROUP BY LOCATION_ID;

--13)WAQD EMPLOYEES DETAILS, DEPARTMENT WISE EMPLOYEE COUNT,HIGHEST SALARY,LOWEST SALARY,AVG ROUNDED SALARY,TOTAL SALARY
SELECT DEPARTMENT_ID,COUNT(*) AS COUNT_OF_EMPLOYEES,
MAX(SALARY) AS MAXIMUM_SALARY,MIN(SALARY) AS MINIMUM_SALARY,
ROUND(AVG(SALARY)) AS AVERAGE_SALARY,SUM(SALARY) AS TOTAL_SALARY
FROM EMPLOYEES
GROUP BY DEPARTMENT_ID;

--14)WAQD DEPARTMENT 50,60,70,80,90 WISE COUNT OF EMPLOYEES AND SORT BY HIGHEST TO LOW
SELECT DEPARTMENT_ID,COUNT(*) AS COUNT_OF_EMPLOYEES
FROM EMPLOYEES
WHERE DEPARTMENT_ID IN (50,60,70,80,90) 
GROUP BY DEPARTMENT_ID
ORDER BY COUNT(*) DESC;

--15)WAQD EMPLOYEES SALARY COST TO PAID EVERY MONTH AND EVERY YEAR.
SELECT SUM(SALARY)AS MONTHLY_PAID
FROM EMPLOYEES;

--16)WAQD DEPARTMENT ID VIZ ANIVAL INCOME SORT BY DESCENDING ORDER. 
SELECT  DEPARTMENT_ID,SUM(SALARY*12)AS DEP_VIZ_ANIVAL_SALARY
FROM EMPLOYEES
GROUP BY DEPARTMENT_ID
ORDER BY SUM(SALARY*12)DESC;

--17)WAQD JOB VIZ MINMUM SALARY AND MAXIMUM SALARY SORDED BY SALARY IN DESCENDING.
SELECT JOB_ID,MIN(SALARY)AS MIN_SALARY,MAX(SALARY)AS MAX_SALARY
FROM EMPLOYEES
GROUP BY JOB_ID
ORDER BY MIN(SALARY)DESC,MAX(SALARY)DESC;

--18)WAQD EMPLOYEES JOINNIG WHICH MONTH HOW MANY EMPLOYEES HIRE.
SELECT TO_CHAR(HIRE_DATE,'MONTH')AS JOIN_MONTHS,COUNT(*)AS TOTAL_EMPLOYEES 
FROM EMPLOYEES
GROUP BY TO_CHAR(HIRE_DATE,'MONTH')
ORDER BY COUNT(*)DESC;
 
--19)WAQD EMPLOYEES HIRE_MONTH AND SHOW WHICH MONTH HOW MANY EMPLOYEES HIRE AND SHOWS EVERY MONTH MINMUM HIRE DATE AND MAXZIMUM 
SELECT TO_CHAR(HIRE_DATE,'MONTH')AS HIRE_MONTH,COUNT(*)AS TOTAL_EMP,
MIN(HIRE_DATE)AS MIN_HIRE_DATE,MAX(HIRE_DATE)AS MAX_HIRE_DATE 
FROM EMPLOYEES
GROUP BY TO_CHAR(HIRE_DATE,'MONTH');
 
--20)WAQD EMPLOYEES MANAGER AND HOW MANY EMPLOYEES WORK UNDER.
SELECT MANAGER_ID,COUNT(*)AS TOTAL_EMP
FROM EMPLOYEES
WHERE MANAGER_ID IS NOT NULL
GROUP BY MANAGER_ID;

--21)WAQD EMPLOYEES HIRED MONTH IN 'JUNE' AHD TOTAL COUNT OF EMPLOYEES. 
SELECT TO_CHAR(HIRE_DATE,'MONTH')AS HIRE_MONTH,COUNT(*)AS EMP_COUNT
FROM EMPLOYEES
WHERE TRIM(TO_CHAR(HIRE_DATE,'MONTH'))='JUNE'
GROUP BY TO_CHAR(HIRE_DATE,'MONTH');


--22)WAQD EMPLOYEES HIRE_DAY VIZ COUNT SORTED BY TOTAL COUNT IN DESCENDING.
SELECT TO_CHAR(HIRE_DATE,'DAY')AS WEEK_DAY,COUNT(*)AS TOTAL_EMP 
FROM EMPLOYEES 
GROUP BY TO_CHAR(HIRE_DATE,'DAY')
ORDER BY COUNT(*)DESC;

--23)WAQD EMPLOYEES HIRED MONTH IN NOT IN 'JUNE','JANUARY','FEBRUARY ','AUGUST' AHD TOTAL COUNT OF EMPLOYEES. 
SELECT TO_CHAR(HIRE_DATE,'MONTH')AS HIRE_MONTH,COUNT(*)AS EMP_COUNT
FROM EMPLOYEES
WHERE TRIM(TO_CHAR(HIRE_DATE,'MONTH')) NOT IN('JUNE','JANUARY','FEBRUARY ','AUGUST')
GROUP BY TO_CHAR(HIRE_DATE,'MONTH');

--24)WAQD SHOWS THOSE EMPLOYEE HIRED SATURDAY,THURSDAY,WEDNESDAY,TUESDAY,SUNDAY AND DAY VIZ COUNT SORTED BY TOTAL COUNT IN DESCENDING.
SELECT TO_CHAR(HIRE_DATE,'DAY')AS WEEK_DAY,COUNT(*)AS TOTAL_EMP 
FROM EMPLOYEES 
WHERE TRIM(TO_CHAR(HIRE_DATE,'DAY')) 
IN('SATURDAY','THURSDAY','WEDNESDAY','TUESDAY','SUNDAY')
GROUP BY TO_CHAR(HIRE_DATE,'DAY')
ORDER BY COUNT(*)DESC;

--25)WAQD LOCATIONS_ID AND WHICH LOCATIONS HOW MEANY DEPARTMENTS. 
SELECT LOCATION_ID,COUNT(*)AS DEPARTMENT_COUNT 
FROM DEPARTMENTS
GROUP BY LOCATION_ID
ORDER BY LOCATION_ID DESC;

--19)WAQD DETAILS OF EMPLOYEES FULL_NAME ,SALARY IS MORE THAN 3000 AND HAVING DEPARTMENT_ID 10,20,30,40
SELECT (LPAD(CONCAT(CONCAT(FIRST_NAME,' '),LAST_NAME),20,'*')) AS DETAILS, SALARY, DEPARTMENT_ID

--22)WAQD DETAILS OF EMPLOYEES WHOSE FIRST_NAME LENGTH IS LESS THAN OR EQUAL TO 5
SELECT LENGTH(FIRST_NAME) AS LENGTH, FIRST_NAME
FROM EMPLOYEES
WHERE LENGTH(FIRST_NAME) <= 5;

--23)WAQD DETAILS OF EMPLOYEES WHOSE FIRST_NAME AND LAST_NAME INITIAL WITH SALARY MORE THAN 2000 AND ORDER BY HIRE_DATE
SELECT UPPER(FIRST_NAME || '.' || SUBSTR(LAST_NAME,1,1)) AS LABEL_NAME, HIRE_DATE, JOB_ID, SALARY
FROM EMPLOYEES
WHERE SALARY > 2000
ORDER BY HIRE_DATE;

--24)WAQD TO DETAILS OF EMPLOYEES WHOSE FIRST_NAME AND LAST_NAME STARTS WITH SAME LETTER
SELECT FIRST_NAME ,LAST_NAME 
FROM EMPLOYEES
WHERE SUBSTR(FIRST_NAME,1,1) = SUBSTR(LAST_NAME,1,1);

--25)WAQD TO DETAILS OF EMPLOYEES WHERE FIRST_NAME PADDED ON BOTH SIDES TO ALIGN IN REPORTS

--17)WAQD DETAILS OF EMPLOYEE WHERE FULL_NAME SHOULD BE CASE SENSITIVE AND WHOSE NAME START WITH A 
SELECT INITCAP(FIRST_NAME || ' ' || LAST_NAME) AS FULL_NAME
FROM EMPLOYEES
WHERE FIRST_NAME LIKE 'A%';

--18)WAQD DETAILS OF EMPLOYEES FIRST_NAME AND LAST_NAME INITIALS IN UPPER
SELECT UPPER(SUBSTR(FIRST_NAME, 1, 1) || '.' || SUBSTR(last_name, 1, 1)) AS SHORT_NAME, SALARY, JOB_ID, DEPARTMENT_ID

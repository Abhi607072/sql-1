--20)WAQD DETAILS OF EMPLOYEES FULL NAME IN UPPER , TRIMMED, AND PADDED TO LENGTH 25
SELECT RPAD(TRIM(UPPER(FIRST_NAME || ' ' || LAST_NAME)),25,'*') AS FULL_NAME

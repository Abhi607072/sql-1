--1)WAQD EMPLOYEES FULL NAME AND THEIR ANNUAL SALARY
SELECT (FIRST_NAME||' '||LAST_NAME) AS FULL_NAME,(SALARY*12) AS ANNUAL_SALARY
FROM EMPLOYEES;

--2)WAQD 'EMPLOYEE 100 IS STEVAN KING WHO WORK AS JOB_ID IN DEPARTMENT 90 WITH SALARY 24000'
SELECT ('EMPLOYEE '||EMPLOYEE_ID||' IS '||FIRST_NAME||' '||LAST_NAME||' WHO WORK AS '||JOB_ID||' IN DEPARTMENT '||DEPARTMENT_ID
        ||' WITH SALARY '||SALARY) AS EMPLOYEES_DETAILS
FROM EMPLOYEES;

--3)WAQD EMPLOYEES HIRE DETAIL IS A SENTANCE
SELECT (FIRST_NAME||' '||LAST_NAME||' WHO HIRE IN '||HIRE_DATE) AS HIRE_DETAILS
FROM EMPLOYEES;

--4)WAQD EMPLOYEES WHO WORK AS IT_PROG
SELECT * 
FROM EMPLOYEES
WHERE JOB_ID = 'IT_PROG';

--5)WAQD EMPLOYEES DETAILS WHO FIRST NAME STARTS WITH LETTER P AND THEIR SALARY MORE THAN 5000
SELECT *
FROM EMPLOYEES
WHERE FIRST_NAME LIKE 'P%'
AND SALARY > 5000;

--6)WAQD EMPLOYEES DETAILS WHO WORK IN DEPARTMENT 50,60,70,80 AND SORT BY DEPARTMENT ID
SELECT * 
FROM EMPLOYEES
WHERE DEPARTMENT_ID IN (50,60,70,80)
ORDER BY DEPARTMENT_ID;

--7)WAQD EMPLOYEES DETAILS WHO HIRED IN YEAR 2005
SELECT *
FROM EMPLOYEES
WHERE TO_CHAR(HIRE_DATE,'YYYY') = 2005;

--8)WAQD EMPLOYEES DETAILS WHO ARE NOT ELIGIBLE FOR COMMISSION
SELECT *
FROM EMPLOYEES
WHERE COMMISSION_PCT IS NULL;

--9)WAQD EMPLOYEES DETAILS WHOSE MANAGER ID IS 100 AND SALARY MORE THAN 10000 AND SORT BY SALARY HIGH TO LOW
SELECT *
FROM EMPLOYEES
WHERE MANAGER_ID = 100
AND SALARY > 10000
ORDER BY SALARY DESC;

--10)WAQD EMPLOYEES FULL NAME AND THEIR ANNUAL SALARY WITH INCLUDING COMMISSION WHO ARE ELIGILBE
SELECT CONCAT(CONCAT(FIRST_NAME,' '),LAST_NAME) AS FULL_NAME,((SALARY+(SALARY*COMMISSION_PCT))*12) AS ANNUAL_SALARY
FROM EMPLOYEES
WHERE COMMISSION_PCT IS NOT NULL;

--11)WAQD EMPLOYEES DETAILS WHO WORK AS CLERK IN A COMPANY AND SORT BY SALARY
SELECT *
FROM EMPLOYEES
WHERE JOB_ID LIKE '%CLERK'
ORDER BY SALARY;

--12)WAQD HOW MANY EMPLOYEES HIRED IN A EVERY YEAR AN DSORT BY YEAR
SELECT TO_CHAR(HIRE_DATE,'YYYY') AS HIRED_YEAR,COUNT(*) AS EMPLOYEES_COUNT
FROM EMPLOYEES
GROUP BY TO_CHAR(HIRE_DATE,'YYYY')
ORDER BY HIRED_YEAR;

--13)WAQD EMPLOYEES DETAILS LIKE FULL NAME ALONG WITH THEIR ROUNDED EXPERIENCE
SELECT CONCAT(CONCAT(FIRST_NAME,' '),LAST_NAME) AS FULL_NAME,
       ROUND(MONTHS_BETWEEN(SYSDATE,HIRE_DATE)/12) AS EXPERIENCE
FROM EMPLOYEES;

--14)WAQD DEPARTMENT WISE COUNT OF EMPLOYEES AND DISPLAY WHOES COUNT MORE THAN 5
SELECT DEPARTMENT_ID,COUNT(*) AS EMPLOYEE_COUNT
FROM EMPLOYEES
GROUP BY DEPARTMENT_ID
HAVING COUNT(*) > 5;

--15)WAQD FIND HOW MANY EMPLOYEES WORK AS SAME PROFESSION AND SORT BY NUMBER OF EMPLOYEES
SELECT JOB_ID,COUNT(*) AS EMPLOYEES_COUNT
FROM EMPLOYEES
GROUP BY JOB_ID
ORDER BY COUNT(*);

--16)WAQD EMPLOYEES DETAILS WHOSE FIRST NAME EXACT LENGTH IS 5 
SELECT *
FROM EMPLOYEES
WHERE LENGTH(FIRST_NAME) = 5;

--17)WAQD EMPLOYEES AVERAGE SALARY DEPARTMENT WISE AND HAVING SALARY MORE THAN 5000 AND SEORT BY DEPARTMENT
SELECT DEPARTMENT_ID,AVG(SALARY) AS AVG_SALARY
FROM EMPLOYEES
GROUP BY DEPARTMENT_ID
HAVING AVG(SALARY) > 5000
ORDER BY DEPARTMENT_ID;

--18)WAQD MONTH WISE HIRED NUMBER OF EMPLOYEES IN YEAR 2006 AND SORT BY COUNT OF EMPLOYEES 
SELECT TO_CHAR(HIRE_DATE,'MONTH') AS HIRED_MONTH,COUNT(*) AS EMPLOYEES_COUNT
FROM EMPLOYEES
WHERE TO_CHAR(HIRE_DATE,'YYYY') = 2006
GROUP BY TO_CHAR(HIRE_DATE,'MONTH')
ORDER BY COUNT(*);

--19)WAQD EMPLOYEES DETAILS WHOSE EXCLUDING 1 AND EMAING LETTERS ARE SAME AS LAST NAME
SELECT *
FROM EMPLOYEES
WHERE SUBSTR(EMAIL,2) = UPPER(LAST_NAME);

--20)WAQD DEPARTMENT WISE COUNT OF EMPLOYEES, MAXIMUM SALARY,MINIMUM SALARY AND SORT BY DEPARTMENT
SELECT DEPARTMENT_ID,COUNT(*) AS EMPLOYEES_COUNT,MAX(SALARY) AS MAX_SALARY,MIN(SALARY) AS MIN_SALARY
FROM EMPLOYEES
GROUP BY DEPARTMENT_ID
ORDER BY DEPARTMENT_ID;

--21)WAQD EMPLOYEES DETAILS LIKE ID,FULL NAME,JOB_ID,DEPARTMENT_ID AND QUTERLY SAALRY
SELECT EMPLOYEE_ID,(FIRST_NAME||' '||LAST_NAME) AS FULL_NAME,JOB_ID,DEPARTMENT_ID,(SALARY*3) AS QUTERLY_SALARY
FROM EMPLOYEES;

--22)WAQD JOB WISE HIGHEST EXPERIENCE OF EMPLOYEES AND SORTED BY HIGH TO LOW ECPERIENCE
SELECT JOB_ID,MAX(ROUND(MONTHS_BETWEEN(SYSDATE,HIRE_DATE)/12)) AS EXPERIENCE
FROM EMPLOYEES
GROUP BY JOB_ID
ORDER BY EXPERIENCE DESC;

--23)WAQD THOSE EMPLOYEES WHOSE MANAGER VIZ AVRAGE SALARY.
SELECT MANAGER_ID,ROUND(AVG(SALARY))AS AVG_SALARY
FROM EMPLOYEES
WHERE MANAGER_ID IS NOT NULL
GROUP BY MANAGER_ID;

--24)WAQD COMMISSION VIZ TOTAL ANUAL SALARY WHERE TOTAL SALARY IS GREATER THAN 120000 IS NOT ELEGBLE FOR COMMISSION
SELECT (SALARY*12)+SALARY*NVL(COMMISSION_PCT,0) AS ANUAL_SALARY,COUNT(*)AS TOTAL_EMP
FROM EMPLOYEES
GROUP BY (SALARY*12)+SALARY*NVL(COMMISSION_PCT,0)
HAVING (SALARY*12)+SALARY*NVL(COMMISSION_PCT,0)<120000;

--25)WAQD LOCATION WISE DEPARTMENT COUNT AND SHOW ONLY THOSE LOCATIONS WHERE COUNT IS MORE THAN 2.
SELECT LOCATION_ID,
COUNT(DEPARTMENT_ID) AS TOTAL_DEPARTMENTS
FROM DEPARTMENTS
GROUP BY LOCATION_ID
HAVING COUNT(DEPARTMENT_ID) > 2;
    
--22)WQAD EMPLOYEES DETAILS SALARY 5 OR UP TO 5 DIGITS AND EXPERIENCE IS BETWEEN 2 AND 5 YEARS AND COMMISSION_PCT UP .2 SORT SALARY DESC.
SELECT EMPLOYEE_ID,FIRST_NAME,LAST_NAME,SALARY,LENGTH(SALARY),COMMISSION_PCT
FROM EMPLOYEES
WHERE LENGTH(SALARY)>=5
AND COMMISSION_PCT>=.2
AND MONTHS_BETWEEN(SYSDATE, HIRE_DATE) BETWEEN 12 AND 40
ORDER BY SALARY;

--23)Write a query to display all EMPLOYEES details in UPPER CASE whose DEPARTMENT_ID is NOT IN 90, 50, 60, whose HIRE_DATE is greater than 07-03-2006, calculate YEARS of service, and order the result by HIRE_DATE.
SELECT EMPLOYEE_ID,UPPER(FIRST_NAME)AS FIRST_NAME,UPPER(LAST_NAME)AS LAST_NAME,
UPPER(EMAIL)AS EMAIL,UPPER(JOB_ID)AS JOB_ID,SALARY,DEPARTMENT_ID,
ROUND(MONTHS_BETWEEN(SYSDATE, HIRE_DATE) / 12, 2) AS YEARS
FROM EMPLOYEES
WHERE DEPARTMENT_ID NOT IN (90, 50, 60)
AND HIRE_DATE > TO_DATE('07-03-2006', 'DD-MM-YYYY')
ORDER BY HIRE_DATE;

--24)WQAD --12)WAQD EMPLOYEES FULL NAME IN UPPER CASE THOSE WOKING NOT 80,60,50 AND CAN NOT GET COMMISSION AND EXPEARENCE SORT THE DEPARTMENT_ID DESCENDING AND SALARY DESC.
SELECT EMPLOYEE_ID,DEPARTMENT_ID,
UPPER(FIRST_NAME||' '||LAST_NAME)AS FULL_NAME,
SALARY,COMMISSION_PCT,
ROUND(MONTHS_BETWEEN(SYSDATE,HIRE_DATE)/12) AS EXPAERENCE
FROM EMPLOYEES
WHERE DEPARTMENT_ID IN (80,60,90)
AND COMMISSION_PCT IS NULL
ORDER BY DEPARTMENT_ID DESC,SALARY DESC;


--1)WQAD EMPLOYEESE HAVING SALARY UP TO 7000  DISPLAY MOD SALARY SALARY BY 10.
SELECT EMPLOYEE_ID,FIRST_NAME,SALARY,MOD(SALARY,10)
FROM EMPLOYEES
WHERE SALARY>7000;

--2)WQAD EMPLOYEES WHOUSE WORING UP 12 YEAR AND HAVINIG SALARY UP TO 10000.
SELECT EMPLOYEE_ID,FIRST_NAME,LAST_NAME,
ROUND(MONTHS_BETWEEN(SYSDATE,HIRE_DATE)/12)AS EXPERIENCE,SALARY
FROM EMPLOYEES
WHERE ROUND(MONTHS_BETWEEN(SYSDATE,HIRE_DATE)/12)>=12
AND SALARY>10000;

--3)WQAD EMPLOYEES WHOUSE WORING UP 12 YEAR AND HAVINIG SALARY UP TO 10000 AND GET COMMISSION_PCT .2.
SELECT EMPLOYEE_ID,FIRST_NAME,LAST_NAME,
ROUND(MONTHS_BETWEEN(SYSDATE,HIRE_DATE)/12)AS EXPERIENCE,SALARY
FROM EMPLOYEES
WHERE SALARY>10000
AND COMMISSION_PCT>=.2;

--4)WQAD TO FIND DAY OF HIREING NEXT MONDAY AND LAST DAY OF THIS MONTH.
SELECT EMPLOYEE_ID,FIRST_NAME,LAST_NAME,SALARY,HIRE_DATE,
NEXT_DAY(HIRE_DATE,'MONDAY')AS TITAL,LAST_DAY(HIRE_DATE) 
FROM EMPLOYEES;

--5)WQAD EMPLOYEES WHOUSE EXPERIENCE UP TO 6 YEAR THEN GIVE .2 PERSENTAGE BONUSE . 
SELECT EMPLOYEE_ID,FIRST_NAME,LAST_NAME,
ROUND(MONTHS_BETWEEN(SYSDATE,HIRE_DATE))AS EXPERIENCE,SALARY,SALARY*.2 AS BONUSE
FROM EMPLOYEES
WHERE ROUND(MONTHS_BETWEEN(SYSDATE,HIRE_DATE))>6;

--6)WQAD EMPLOYEES HAVING A 6 MONTH INTERNSHIPS SHOW THE LAST DATE OF INTERNSHIP.

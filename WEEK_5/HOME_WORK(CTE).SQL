 ON(D.DEPARTMENT_ID = F.DEPARTMENT_ID);
 
---4)WAQD THOSE DEPARTMENT WHOUSE AVG SALARY MORE THAN TOLAL SALARY ALONG WITH DEPARTMET NAME.
WITH DEPT_AVG_SAL
AS
(
SELECT DEPARTMENT_ID , AVG(SALARY) AS AVG_SAL
FROM EMPLOYEES
GROUP BY DEPARTMENT_ID
),
DEPT_TOTAL_SAL
AS
(
SELECT DEPARTMENT_ID , SUM(SALARY) AS TOTAL_SAL
FROM EMPLOYEES
GROUP BY DEPARTMENT_ID
)
SELECT t1.DEPARTMENT_ID,ROUND(t1.AVG_SAL)AS AVG_SAL,t2.TOTAL_SAL, d.DEPARTMENT_NAME
FROM DEPT_AVG_SAL t1  JOIN DEPT_TOTAL_SAL t2
ON(t1.DEPARTMENT_ID = t2.DEPARTMENT_ID
AND (t1. AVG_SAL) < t2.TOTAL_SAL )
JOIN DEPARTMENTS D 
ON(D.DEPARTMENT_ID = t1.DEPARTMENT_ID );


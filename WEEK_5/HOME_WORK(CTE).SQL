---1)WAQD EMPLOYEES EXPERENCES WHOSE EXPERENCES MORE THAN 15 YEARS AND IS NOT MANAGER.

WITH EMP_EXP
AS 
 (SELECT EMPLOYEE_ID,FIRST_NAME,LAST_NAME,ROUND((MONTHS_BETWEEN(SYSDATE,HIRE_DATE)/12)) AS EXP 
  FROM EMPLOYEES
  WHERE (ROUND(MONTHS_BETWEEN(SYSDATE,HIRE_DATE)/12)) > 15
)
SELECT EMPLOYEE_ID,FIRST_NAME,LAST_NAME,EXP
FROM EMP_EXP
WHERE EMPLOYEE_ID NOT IN ( SELECT DISTINCT MANAGER_ID FROM EMPLOYEES );

---2)WAQD EMPLOYEES DEPARTMENT WHOSE WORKING EMPLOYEES MORE THAN 10 AND
--   DEPARTMENT_ID BETWEEN 10,20,30,50,80,90.
WITH EMP_DPT
AS 
  (SELECT EMPLOYEE_ID,FIRST_NAME,LAST_NAME,DEPARTMENT_ID
   FROM EMPLOYEES),
   DEP AS
  (SELECT DEPARTMENT_ID,DEPARTMENT_NAME
   FROM DEPARTMENTS
   WHERE DEPARTMENT_ID IN (10,20,30,50,80,90)
  )
    SELECT E.DEPARTMENT_ID,D.DEPARTMENT_NAME,COUNT(*) AS DEPAT_CNT
    FROM EMP_DPT E JOIN DEP D
    ON(E.DEPARTMENT_ID = D.DEPARTMENT_ID)
    GROUP BY E.DEPARTMENT_ID,D.DEPARTMENT_NAME
    HAVING COUNT(*)>10;
    
---3)WAQD THOSE EMPLOYEES WHOSE SALARY MORE THAN 7000 AND FIRST_NAME 
--   START WITH 'A' AND SHOW ALL DETAILS ALONG WITH DEPARTMENT NAME.
WITH EMP_001
AS 
(SELECT EMPLOYEE_ID,FIRST_NAME,LAST_NAME,SALARY,DEPARTMENT_ID
 FROM EMPLOYEES
 WHERE SALARY>7000),
 EMP_002
 AS
 (SELECT EMPLOYEE_ID,FIRST_NAME,LAST_NAME,SALARY,DEPARTMENT_ID
 FROM EMPLOYEES
 WHERE FIRST_NAME LIKE 'A%')
 SELECT  F.EMPLOYEE_ID,F.FIRST_NAME,F.LAST_NAME,F.SALARY,F.DEPARTMENT_ID,D.DEPARTMENT_NAME
 FROM EMP_001 E JOIN EMP_002 F
 ON(E.EMPLOYEE_ID = F.EMPLOYEE_ID)

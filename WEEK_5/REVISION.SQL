--7)WAQD THEIRD HIGHEST SALARY FOR EACH DEPARTMENT
SELECT D.DEPARTMENT_NAME,MAX(E.SALARY)AS MAX_SALARY,COUNT(*) AS CNT
FROM EMPLOYEES E JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
GROUP BY D.DEPARTMENT_NAME
HAVING COUNT(*) > 3
AND MAX(E.SALARY) >ALL ( SELECT MAX(SALARY)
                   FROM EMPLOYEES E JOIN DEPARTMENTS D
                   ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
                   GROUP BY D.DEPARTMENT_ID
                   HAVING MAX(E.SALARY) >ALL (SELECT MAX(E.SALARY)
                                              FROM EMPLOYEES E JOIN DEPARTMENTS D
                                              ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
                                              GROUP BY D.DEPARTMENT_ID));

--8)WAQD DEPARTMENT AND LOCATIONS COUNT OF EMPLOYEES HAVING MORE THAN 2 EMPLOYEES
SELECT DEPARTMENT_ID, COUNT(*) AS EMPLOYEES_COUNT
FROM EMPLOYEES
GROUP BY DEPARTMENT_ID
HAVING COUNT(*) > 2
UNION ALL
SELECT L.LOCATION_ID, COUNT(*)
FROM EMPLOYEES E JOIN DEPARTMENTS D
ON (E.DEPARTMENT_ID = D.DEPARTMENT_ID)
JOIN LOCATIONS L
ON (D.LOCATION_ID = L.LOCATION_ID)
GROUP BY L.LOCATION_ID
HAVING COUNT(*) > 2
ORDER BY DEPARTMENT_ID;

--9)WAQD EMPLOYEES ID,FIRST_NAME,LAST NAME AND EXPERIENCE AND GIVE ADDITIONAL INCREMENT ON
--SALARY ACCORDING TO EXPERIENCE.
SELECT EMPLOYEE_ID,SALARY*NVL(COMMISSION_PCT,0)AS SALARY,
       ROUND(MONTHS_BETWEEN(SYSDATE,HIRE_DATE)/12) AS EXPERIENCE,
CASE WHEN ROUND(MONTHS_BETWEEN(SYSDATE,HIRE_DATE)/12) BETWEEN 1 AND 5 THEN SALARY*.2
     WHEN ROUND(MONTHS_BETWEEN(SYSDATE,HIRE_DATE)/12) BETWEEN 6 AND 10 THEN SALARY*.3
     WHEN ROUND(MONTHS_BETWEEN(SYSDATE,HIRE_DATE)/12) BETWEEN 11 AND 16 THEN SALARY*.4
     WHEN ROUND(MONTHS_BETWEEN(SYSDATE,HIRE_DATE)/12) BETWEEN 17 AND 24 THEN SALARY*.5
     ELSE SALARY*.7
     END AS BONUSE
FROM EMPLOYEES;


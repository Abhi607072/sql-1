/* 2)WAQ TO USE ONE TABLE EMPLOYEES AND ONE DEPARTMENTS VIEW FETCH RECORD USING SUBQUERY
(DISPLAY EMPLOYEE_ID,FIRST_NAME,LAST_NAME,DEPARTMENT ID,DEPARTMENT NAME). */
SELECT E.EMP_ID,E.FIRST_NAME,E.LAST_NAME,E.DEPARTMENT_ID,W.DEPARTMENT_NAME
FROM EMP E JOIN DEPT_VIEW W
ON(E.DEPARTMENT_ID=W.DEPARTMENT_ID)
WHERE E.DEPARTMENT_ID IN(SELECT E.DEPARTMENT_ID
                     FROM EMP
                     WHERE E.FIRST_NAME='RAM');
                     
--3)WAQD EMPLOYEES MANAGERS MANAGER.
SELECT E.FIRST_NAME AS EMPLOYEE_NAME, M.FIRST_NAME AS MANAGER_NAME,S.FIRST_NAME AS SENIOR_MANAER_NAME
FROM EMPLOYEES E JOIN EMPLOYEES M
ON (E.MANAGER_ID = M.EMPLOYEE_ID)
JOIN EMPLOYEES S
ON (M.MANAGER_ID = S.EMPLOYEE_ID);


--4)CREATE A COMPLEX VIEW TO USE ONE TABLE EMPLOYEES AND ONE DEPARTMENTS VIEW FEACH RECORD 
--USING SUBQUERY(DESPLAY EMPLOYEE_ID,FIRST_NAME,LAST_NAME,DEPARTMENT ID,DEPARTMENT NAME)
--TO FIND THOSE EMPLOYEES WHOSE DEPARTMENT SIMILER TO 'RAM'S DEPARTMENT .
CREATE VIEW DEP_EMP1_VIEW AS SELECT E.EMP_ID,E.FIRST_NAME,
E.LAST_NAME,E.DEPARTMENT_ID,W.DEPARTMENT_NAME
FROM EMP E JOIN DEPT_VIEW W
ON(E.DEPARTMENT_ID=W.DEPARTMENT_ID)
WHERE E.DEPARTMENT_ID IN(SELECT E.DEPARTMENT_ID
                     FROM EMP
                     WHERE E.FIRST_NAME='RAM');
                          
--5)WAQD EMPLOYEE_NAME,MANAGER_NAME,SENIOR_MANAER_NAME,HADE_OF_MANAGER
SELECT E.FIRST_NAME AS EMPLOYEE_NAME, M.FIRST_NAME AS MANAGER_NAME,S.FIRST_NAME AS SENIOR_MANAER_NAME,
       H.FIRST_NAME AS HADE_OF_MANAGER
FROM EMPLOYEES E JOIN EMPLOYEES M
ON (E.MANAGER_ID = M.EMPLOYEE_ID)
JOIN EMPLOYEES S
ON (M.MANAGER_ID = S.EMPLOYEE_ID)
JOIN EMPLOYEES H
ON (S.MANAGER_ID = H.EMPLOYEE_ID);


--SHOWS EMPLOYEES DETAILS WITH COUNT OF EMPLOYEES AS PER DEPARTMENTS.
SELECT EMPLOYEE_ID ,FIRST_NAME,DEPARTMENT_ID,COUNT(*) over (partition by department_id)as cnt
FROM EMPLOYEES ;

--1)WAQD EMPLOYEES TOP 3RD SALARY
--METHOD 1
WITH SAL AS
(SELECT EMPLOYEE_ID,FIRST_NAME,LAST_NAME,SALARY,DEPARTMENT_ID,
DENSE_RANK() OVER(ORDER BY SALARY DESC)AS DEP_WIZE_TOP_SAL
FROM EMPLOYEES)
SELECT * FROM SAL
WHERE DEP_WIZE_TOP_SAL<=3;

--METHOD 2
SELECT * 
FROM (SELECT EMPLOYEE_ID,FIRST_NAME,LAST_NAME,SALARY,DEPARTMENT_ID,
DENSE_RANK() OVER(ORDER BY SALARY DESC)AS RNK
FROM EMPLOYEES )
WHERE RNK <= 3;


--2)WAQD DEPARTMENTS WIZE TOP 3 SALARY'S
-- METHOD 1
WITH SAL AS 
(SELECT EMPLOYEE_ID, FIRST_NAME,LAST_NAME,SALARY,DEPARTMENT_ID,
DENSE_RANK() OVER (PARTITION BY DEPARTMENT_ID ORDER BY SALARY DESC) AS RNK
FROM EMPLOYEES)
SELECT *  
FROM SAL
WHERE RNK <= 3;

--METHOD 2
SELECT *
FROM ( SELECT EMPLOYEE_ID,FIRST_NAME,LAST_NAME,SALARY,DEPARTMENT_ID,
DENSE_RANK()OVER(PARTITION BY DEPARTMENT_ID ORDER BY SALARY DESC)AS RNK 
FROM EMPLOYEES)
WHERE RNK <=3;


--3)WAQD BOTTEM 3 SALARY'S

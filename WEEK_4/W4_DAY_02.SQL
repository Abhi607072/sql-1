--1)WAQD EMPLOYEES WHOSE HAVING SALARY BETWEEN 1 TO 2000 THEN SHOWS 'MORE LESS SALARY',THEN SALARY BETWEEN 2001 TO 5000 THEN IT SHOW 'LESS SALARY',WHEN SALARY BETWEEN 5001 TO 10000 THEN SHOW 'AVREAGE SALARY', WHEN SALARY BETWEEN 10001 TO 2000 THEN SHOW 'GOOD SALARY' ELES DISPLAY 'VERY GOOD SALARY'.
SELECT EMPLOYEE_ID,FIRST_NAME,LAST_NAME,SALARY,
CASE WHEN SALARY BETWEEN 1 AND 2000 THEN 'MORE LESS SALARY'
     WHEN SALARY BETWEEN 2001 AND 5000 THEN 'LESS SALARY'
     WHEN SALARY BETWEEN 5001 AND 10000 THEN 'AVREAGE SALARY'
     WHEN SALARY BETWEEN 10001 AND 2000 THEN 'GOOD SALARY'
     ELSE 'VERY GOOD SALARY'
     END AS SALARY_TITAL
FROM EMPLOYEES;

--2)WAQD EMPLOYEES WHOSE HAVING  ANNUAL SALARY BETWEEN 1 TO 36000 THEN SHOWS 'MORE LESS SALARY',THEN SALARY BETWEEN 36001 TO 50400 THEN IT SHOW 'LESS SALARY',WHEN SALARY BETWEEN  50401 TO 100800 THEN SHOW 'AVREAGE SALARY', WHEN SALARY BETWEEN 100801 TO 203999 THEN SHOW 'GOOD SALARY' ELES DISPLAY 'VERY GOOD SALARY'.
SELECT EMPLOYEE_ID,FIRST_NAME,LAST_NAME,SALARY*12 AS ANNUAL_SALARY,
CASE WHEN SALARY*12 BETWEEN 1 AND 36000 THEN 'MORE LESS SALARY'
     WHEN SALARY*12 BETWEEN 36001 AND 50400 THEN 'LESS SALARY'
     WHEN SALARY*12 BETWEEN 50401 AND 100800 THEN 'AVREAGE SALARY'
     WHEN SALARY*12 BETWEEN 100801 AND 203999 THEN 'GOOD SALARY'
     ELSE 'VERY GOOD SALARY'
     END AS SALARY_TITAL
FROM EMPLOYEES
ORDER BY SALARY DESC;

--3)WAQD EMPLOYEES WHOSE HAVING  ANNUAL SALARY BETWEEN 1 TO 36000 THEN ADD SALARY 500,THEN SALARY BETWEEN 36001 TO 50400 THEN ADD SALARY 1000,WHEN SALARY BETWEEN  50401 TO 100800 THEN ADD SALARY 1500, WHEN SALARY BETWEEN 100801 TO 203999 THEN ADD SALARY 2000 ELES DISPLAY ADD SALARY 3000.
SELECT EMPLOYEE_ID,FIRST_NAME,LAST_NAME,SALARY*12 AS ANNUAL_SALARY,
CASE WHEN SALARY*12 BETWEEN 1 AND 36000 THEN SALARY*12+500
     WHEN SALARY*12 BETWEEN 36001 AND 50400 THEN SALARY*12+1000
     WHEN SALARY*12 BETWEEN 50401 AND 100800 THEN SALARY*12+1500
     WHEN SALARY*12 BETWEEN 100801 AND 203999 THEN SALARY*12+2000
     ELSE SALARY*12+3000
     END AS SAL_ADDIONAL_SALARY
FROM EMPLOYEES
ORDER BY SALARY DESC;


/*4)WAQD EMPLOYEES ID,FIRST_NAME,LAST NAME AND EXPERIENCE AND GIVE ADDITIONAL INCREMENT ON
SALARY ACCORDING TO EXPERIENCE */
SELECT EMPLOYEE_ID,SALARY*COMMISSION_PCT,ROUND(MONTHS_BETWEEN(SYSDATE,HIRE_DATE)/12) AS EXPERIENCE,
CASE WHEN ROUND(MONTHS_BETWEEN(SYSDATE,HIRE_DATE)/12) BETWEEN 1 AND 5 THEN SALARY*.2
     WHEN ROUND(MONTHS_BETWEEN(SYSDATE,HIRE_DATE)/12) BETWEEN 6 AND 10 THEN SALARY*.3
     WHEN ROUND(MONTHS_BETWEEN(SYSDATE,HIRE_DATE)/12) BETWEEN 11 AND 16 THEN SALARY*.4
     WHEN ROUND(MONTHS_BETWEEN(SYSDATE,HIRE_DATE)/12) BETWEEN 17 AND 24 THEN SALARY*.5
     ELSE SALARY*.7
     END AS BONUSE
FROM EMPLOYEES;

--5)WAQD EMPLOYEES ID ,FIRST_NAME,LAST_NAME,SALARY,DEPARTMENT NAME, COMMISSION_PCT SHOW ONLY THOSE EMPLOYEES WHO HAVING COMMISSION AND ADD COMMISION PERSENTAGE ON ACCORDING TO COMMITON. 
SELECT E.EMPLOYEE_ID,E.FIRST_NAME,E.LAST_NAME,E.SALARY,D.DEPARTMENT_NAME,
NVL(COMMISSION_PCT,0)AS COMMSSION_PCT,
CASE WHEN COMMISSION_PCT BETWEEN 0.01 AND 0.3 THEN SALARY*COMMISSION_PCT 
     WHEN COMMISSION_PCT BETWEEN 0.4 AND 0.6 THEN SALARY*COMMISSION_PCT
     ELSE SALARY*COMMISSION_PCT
     END AS COMMISSION
FROM EMPLOYEES E JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
WHERE COMMISSION_PCT IS NOT NULL;


--6)WAQD EMPLOYEES  DETAILS LOCATIONS_ID WIZE RIGIONS AND COUNTRY ID 'IT','US','UK','CH' SORTED BY EMPLOYEES ID.
SELECT E.EMPLOYEE_ID,(FIRST_NAME||' '||LAST_NAME)AS FULL_NAME,L.CITY,L.LOCATION_ID, 
CASE WHEN L.LOCATION_ID BETWEEN 1000 AND 1500 THEN 'EAST'
     WHEN L.LOCATION_ID BETWEEN 1501 AND 2000 THEN 'SOUTH'
     WHEN L.LOCATION_ID BETWEEN 2001 AND 3000 THEN 'WEAST'
     ELSE 'NORTH'
     END AS REIGION
FROM EMPLOYEES E JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
JOIN LOCATIONS L
ON(D.LOCATION_ID=L.LOCATION_ID)
WHERE COUNTRY_ID IN('IT','US','UK','CH')
ORDER BY EMPLOYEE_ID;

--7)WAQD EMPLOYEES NAME WITH MANAGER NAME THOSE EMPLOYEES NOT HAVING A MANAGER SHOW NO MANAGER.
SELECT DISTINCT(D.DEPARTMENT_ID), D.DEPARTMENT_NAME, D.MANAGER_ID,
       CASE WHEN D.MANAGER_ID = M.EMPLOYEE_ID THEN CONCAT(CONCAT(M.FIRST_NAME,' '),M.LAST_NAME)
       ELSE 'NO MANAGER'
       END AS MANAGER_DETAIL
FROM EMPLOYEES E JOIN EMPLOYEES M
ON (E.MANAGER_ID = M.EMPLOYEE_ID)
RIGHT OUTER JOIN
DEPARTMENTS D
ON (M.DEPARTMENT_ID = D.DEPARTMENT_ID)
ORDER BY DEPARTMENT_ID;


--8)WAQD EMPLOYEES ID ,FIRST_NAME,LAST_NAME,SALARY,DEPARTMENT NAME, COMMISSION_PCT SHOW ONLY THOSE EMPLOYEES WHO HAVING COMMISSION AND ADD COMMISION PERSENTAGE ON ACCORDING TO COMMITON DIWALI BONUSE ALL EMPLOYEES 1500. 
SELECT E.EMPLOYEE_ID,E.FIRST_NAME,E.LAST_NAME,E.SALARY,D.DEPARTMENT_NAME,
NVL(COMMISSION_PCT,0)AS COMMSSION_PCT,
CASE WHEN COMMISSION_PCT BETWEEN 0.01 AND 0.3 THEN SALARY*NVL(COMMISSION_PCT,0)+1500 
     WHEN COMMISSION_PCT BETWEEN 0.4 AND 0.6 THEN SALARY*NVL(COMMISSION_PCT,0)+1500
     ELSE SALARY*NVL(COMMISSION_PCT,0)+1500
     END AS COMMISSION_WITH_BONUS
FROM EMPLOYEES E JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID);

--9)WAQD EMPLOYEES  DETAILS LOCATIONS_ID WIZE RIGIONS AND CITY NOT IN'IT','US','UK','CH' SORTED BY EMPLOYEES ID.
SELECT E.EMPLOYEE_ID,(FIRST_NAME||' '||LAST_NAME)AS FULL_NAME,L.CITY,L.LOCATION_ID, 
CASE WHEN L.LOCATION_ID BETWEEN 1000 AND 1500 THEN 'EAST'
     WHEN L.LOCATION_ID BETWEEN 1501 AND 2000 THEN 'SOUTH'
     WHEN L.LOCATION_ID BETWEEN 2001 AND 3000 THEN 'WEAST'
     ELSE 'NORTH'
     END AS REIGION
FROM EMPLOYEES E JOIN DEPARTMENTS D
ON(E.DEPARTMENT_ID=D.DEPARTMENT_ID)
JOIN LOCATIONS L
ON(D.LOCATION_ID=L.LOCATION_ID)
WHERE L.CITY NOT IN('Roma','Seattle','Oxford','Singapore')
ORDER BY EMPLOYEE_ID;


--10)WAQD DETAILS OF EMPLOYEES ALONG WITH THEIR DEPARTMENT NAME USING DECODE.
SELECT EMPLOYEE_ID, FIRST_NAME, LAST_NAME, SALARY, E.DEPARTMENT_ID,
       DECODE(E.DEPARTMENT_ID,D.DEPARTMENT_ID,DEPARTMENT_NAME) AS DEPARTMENT_NAME
FROM EMPLOYEES E JOIN DEPARTMENTS D
ON (E.DEPARTMENT_ID = D.DEPARTMENT_ID);

